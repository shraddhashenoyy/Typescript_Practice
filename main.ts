//VARIABLE DECLARATION
// Using "let" keyword
// 1.
let num:number=100;
num=292;
console.log(num);
//2.
let number1:number=9854;
console.log(number1);
// 3.
let str:string="Shraddha";
console.log(str);
// 4.
let str1:string="shenoy";
console.log(str1);

//Using "Const" keyword
// 1.
const name="shraddhaaa";
console.log(name);
// 2.
const name1="shenoyyy";
console.log(name1);
const PI=3.142;
console.log(`The value of PI is ${PI}`);
// 3.
const title="Welcome to this Typescript course";
console.log(title);

